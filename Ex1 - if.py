'''Ler um número inteiro de  dizer se é par ou ímpar.
'''
numero = int(input("Digite o numero inteiro(diferente de  0): "))

if numero%2 == 0:
             print("O número %d é  um número par" %numero)
else:
             print("O número %d é  um número ímpar" %numero)
